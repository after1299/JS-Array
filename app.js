let friends = ["John", "Sandy", "Alex"]
console.log(friends[1]);
console.log(friends.length);

// push
friends.push("Harry");
console.log(friends);
console.log(friends.length);

// pop
friends.pop();
console.log(friends);

// shift
friends.shift();
console.log(friends);

//unshift
friends.unshift("Harry");
console.log(friends);